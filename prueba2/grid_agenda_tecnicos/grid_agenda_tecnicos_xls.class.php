              $this->id_tecnico = substr($this->id_tecnico, 0, $tmp_pos);
          }
          $this->valor_total = (isset($Busca_temp['valor_total'])) ? $Busca_temp['valor_total'] : ""; 
          $tmp_pos = (is_string($this->valor_total)) ? strpos($this->valor_total, "##@@") : false;
